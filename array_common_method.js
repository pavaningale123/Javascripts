// push 👉 Adds element at the end
let arry=[12,23,34,6]
arry.push(56);
console.log(arry);

//pop 👉 Removes last element
arry.pop()
console.log(arry)

//shift 👉 Removes first element 
arry.shift()
console.log(arry)

//unshift  👉 Adds element at beginning
arry.unshift(100);
console.log(arry)

//sort 
arry.sort()
console.log(arry)

//reverse
arry.reverse()
console.log(arry)